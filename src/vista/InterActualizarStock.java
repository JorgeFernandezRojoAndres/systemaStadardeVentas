//metodo para cargar los productos en el jCombobox
    private void CargarComboProducto() {
        Connection cn = Conexion.conectar();
        String sql = "select * from tb_producto";
        Statement st;
        try {
            st = cn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            jComboBox1_producto.removeAllItems();
            jComboBox1_producto.addItem("Seleccion producto:");
            while (rs.next()) {
                jComboBox1_producto.addItem(rs.getString("nombre"));
            }

        } catch (SQLException e) {
            System.out.println("Error al cargar los prosuctosen :" + e);
        }
    }

    //Metodo para  mostrar stock del producto seleccionado
    private void MostrarStock() {

        try {
            Connection cn = Conexion.conectar();
            String sql = "select * from tb_producto where nombre ='" + this.jComboBox1_producto.getSelectedItem() + "'";
            Statement st;
            st = cn.createStatement();
            ResultSet rs = st.executeQuery(sql);

            if (rs.next()) {
                idProducto = rs.getInt("idProducto");
                cantidad = rs.getInt("cantidad");
                txt_cantidadActual.setText(String.valueOf(cantidad));
            } else {
                txt_cantidadActual.setText("");
            }

        } catch (SQLException e) {
            System.out.println("Error al obtener stock del producto en :" + e);
        }
    }

    //metodo de validacion de caracteres no numericos
    private boolean validar(String valor) {
        int num;
        try {
            num = Integer.parseInt(valor);
            return true;
        } catch (NumberFormatException e) {
            return false;
        }
    }
