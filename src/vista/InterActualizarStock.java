    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        //validamos seleccion de producto
        if (!jComboBox1_producto.getSelectedItem().equals("Seleccion producto:")) {
            //valida campo vacio
            if (!txt_cantidadActual.getText().isEmpty()) {
                //validamos que el usuario no ingrese otro caracteres
                boolean validacion = validar(txt_cantidadNueva.getText().trim());
                if (validacion == true) {
                    //validamos la cantidad sea mayo a (0)
                    if (Integer.parseInt(txt_cantidadNueva.getText()) > 0) {

                        Producto producto = new Producto();
                        Ctrl_Producto controlProducto = new Ctrl_Producto();
                        int stockActual = Integer.parseInt(txt_cantidadActual.getText().trim());
                        int stockNuevo = Integer.parseInt(txt_cantidadNueva.getText().trim());

                        stockNuevo = stockActual + stockNuevo;
                        producto.setCantidad(stockNuevo);
                        if (controlProducto.ActualizarStock(producto, idProducto)) {
                            JOptionPane.showMessageDialog(null, "Stock Actualizado");
                            jComboBox1_producto.setSelectedItem("Seleccione Producto:");
                            txt_cantidadActual.setText("");
                            txt_cantidadNueva.setText("");
                            this.CargarComboProducto();
                        } else {
                            JOptionPane.showMessageDialog(null, "Error al actualizar stock");
                        }
                    } else {
                        JOptionPane.showMessageDialog(null, "La cantidad no puede ser cero ni negativa");
                    }
                } else {
                    JOptionPane.showMessageDialog(null, " En la cantidad no se admiten Caracteres no numericos  ");
                }
            } else {
                JOptionPane.showMessageDialog(null, " Ingrese una nueva cantidad para sumar en el stock del producto  ");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Seleccione un producto ");

        }
    }//GEN-LAST:event_jButton1ActionPerformed

